let str = "20";
let a = 5;

console.log(str + a);   // "205" "плюс" приводит число к строке 
console.log(str - a);   // 15 "минус" преобразует строку в число, после чего происходит вычитание
console.log(str * "2"); // 40 "умножить" приводит к числам и выполняет умножение
console.log(str / 2);   // 10 "делить" преобразует строку в число и выполняет деление